
let candidate = 'aprovada';

switch(candidate){

    case 'aprovada':
        console.log('Parabéns, você foi aprovada(o)');
    break;

    case 'lista':
        console.log('Você está na nosa lista de espera');
    break;

    case 'reprovada':
        console.log('Você foi reprovada!');
    break;

default:
    console.log('Informação incorreta')
}
